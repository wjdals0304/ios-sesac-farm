import Foundation
import UIKit


extension UIColor {
    
    func getCustomGray() -> UIColor {
        return UIColor(red: 237/255, green: 240/255, blue: 243/255, alpha: 1.0)
    } 
    func getCustomGreen() -> UIColor {
        return UIColor(red: 0/255, green: 159/255, blue: 72/255, alpha: 1.0)
    }
}
